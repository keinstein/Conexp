package conexp.core.compareutils;


public class KeyValuePair {
    public final java.lang.Object key;
    public final java.lang.Object value;

    /**
     * Pair constructor comment.
     */
    public KeyValuePair(Object k, Object v) {
        super();
        key = k;
        value = v;
    }
}
